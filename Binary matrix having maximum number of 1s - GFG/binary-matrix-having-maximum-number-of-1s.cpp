class Solution {
public:
    vector<int> findMaxRow(vector<vector<int>> mat, int N) {
        //code here
        int maxCount=0;
        int minRow =0 ;
        int col =N-1;
        int row=0;
        
        while(col>=0 && row<N){
            if(mat[row][col]==1){
                minRow=row;
                maxCount++;
                col--;
            }
            else{
                row++;
            }
        }
        
        return {minRow , maxCount} ;
    }
};

